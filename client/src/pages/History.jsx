import React, { useEffect, useState } from "react";
import jsPDF from "jspdf";
import autoTable from "jspdf-autotable";

function History() {
  const [history, setHistory] = useState([]);
  const [expanded, setExpanded] = useState({});
  const [loading, setLoading] = useState(true);

  const normalRanges = {
    Age: "20â€“45 years",
    BMI: "18.5â€“24.9",
    BloodPressure: "80â€“130 mmHg",
    DiabetesPedigreeFunction: "< 0.6",
    Glucose: "70â€“140 mg/dL",
    Insulin: "15â€“276 uU/mL",
    Pregnancies: "0â€“10",
    SkinThickness: "10â€“50 mm",
  };

  // Fetch prediction history
  const fetchHistory = async () => {
    try {
      const token = localStorage.getItem("token");
      const res = await fetch("http://localhost:5000/api/predictions/history", {
        headers: { Authorization: `Bearer ${token}` },
      });
      const data = await res.json();

      if (data.success && Array.isArray(data.history)) {
        setHistory(data.history);
      } else {
        console.error("Unexpected API response:", data);
      }
    } catch (err) {
      console.error("Error fetching history:", err);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    fetchHistory();
  }, []);

  //  Toggle Overview Visibility
  const toggleOverview = (id) => {
    setExpanded((prev) => ({ ...prev, [id]: !prev[id] }));
  };

  //  Delete a specific record
  const handleDelete = async (id) => {
    try {
      const token = localStorage.getItem("token");
      await fetch(`http://localhost:5000/api/predictions/delete/${id}`, {
        method: "DELETE",
        headers: { Authorization: `Bearer ${token}` },
      });
      setHistory(history.filter((item) => item._id !== id));
    } catch (error) {
      console.error("Error deleting prediction:", error);
    }
  };

  //  Generate PDF Report
  const downloadPDF = (record) => {
    const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });

    doc.setFont("helvetica", "bold");
    doc.setFontSize(18);
    doc.text("DIABETES PREDICTION REPORT", 14, 20);

    doc.setFont("helvetica", "normal");
    doc.setFontSize(12);
    doc.text("Generated by: Diabetes Predictor - Sahana Reddy", 14, 30);
    doc.text(`Date: ${new Date(record.createdAt).toLocaleString()}`, 14, 37);

    doc.setFontSize(13);
    doc.setTextColor(0, 102, 204);
    doc.text(`Result: ${record.result}`, 14, 50);
    doc.setTextColor(0, 0, 0);

    const overview = record.overview || {};
    const tableData = Object.entries(overview).map(([k, v]) => [
      k,
      v.value,
      normalRanges[k] || "-",
      v.status.replace(/[^\x00-\x7F]/g, ""),
    ]);

    autoTable(doc, {
      startY: 60,
      head: [["Parameter", "Entered Value", "Normal Range", "Status"]],
      body: tableData,
      theme: "grid",
      headStyles: {
        fillColor: [22, 160, 133],
        textColor: 255,
        halign: "center",
        fontStyle: "bold",
      },
      bodyStyles: { textColor: 30 },
      alternateRowStyles: { fillColor: [245, 245, 245] },
      styles: { font: "helvetica", fontSize: 11 },
    });

    const finalY = doc.lastAutoTable.finalY + 10;
    doc.setFontSize(11);
    doc.setTextColor(100);
    doc.text("Â© 2025 Diabetes Predictor | Developed by Sahana Reddy", 14, finalY);

    doc.save("Diabetes_Report.pdf");
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center h-screen bg-gray-900 text-gray-300">
        Loading your history...
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gray-900 text-white px-4 py-8">
      <h1 className="text-4xl font-bold text-center text-teal-400 mb-6">
        ðŸ©¸ Your Diabetes Prediction History
      </h1>

      {history.length === 0 ? (
        <p className="text-center text-gray-400">No predictions found.</p>
      ) : (
        <div className="space-y-6 max-w-3xl mx-auto">
          {history.map((item) => (
            <div
              key={item._id}
              className="bg-gray-800 p-6 rounded-xl shadow-lg border border-gray-700"
            >
              <div className="flex flex-col md:flex-row justify-between items-start md:items-center">
                <div>
                  <h3 className="text-xl font-semibold text-blue-400">
                    {item.result}
                  </h3>
                  <p className="text-gray-400 text-sm mt-1">
                    {new Date(item.createdAt).toLocaleString()}
                  </p>
                </div>

                <div className="flex gap-3 mt-3 md:mt-0">
                  <button
                    onClick={() => toggleOverview(item._id)}
                    className="bg-blue-500 hover:bg-blue-600 px-4 py-2 rounded-lg text-sm"
                  >
                    {expanded[item._id] ? "Hide Overview" : "Show Overview"}
                  </button>
                  <button
                    onClick={() => downloadPDF(item)}
                    className="bg-yellow-500 hover:bg-yellow-600 px-4 py-2 rounded-lg text-sm"
                  >
                    Download
                  </button>
                  <button
                    onClick={() => handleDelete(item._id)}
                    className="bg-red-600 hover:bg-red-700 px-4 py-2 rounded-lg text-sm"
                  >
                    Delete
                  </button>
                </div>
              </div>

              {expanded[item._id] && (
                <div className="mt-4 overflow-x-auto">
                  <table className="w-full text-sm text-left border border-gray-700">
                    <thead className="bg-gray-700 text-gray-300">
                      <tr>
                        <th className="p-2">Parameter</th>
                        <th className="p-2">Value</th>
                        <th className="p-2">Normal Range</th>
                        <th className="p-2">Status</th>
                      </tr>
                    </thead>
                    <tbody>
                      {Object.entries(item.overview || {}).map(([key, val]) => (
                        <tr key={key} className="border-t border-gray-700">
                          <td className="p-2">{key}</td>
                          <td className="p-2">{val.value}</td>
                          <td className="p-2">{normalRanges[key] || "-"}</td>
                          <td className="p-2">{val.status}</td>
                        </tr>
                      ))}
                    </tbody>
                  </table>
                </div>
              )}
            </div>
          ))}
        </div>
      )}
    </div>
  );
}

export default History;
